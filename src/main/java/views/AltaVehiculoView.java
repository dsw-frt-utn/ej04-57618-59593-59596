    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String patente = jTextField1.getText();
        String marcaNombre = (String) jComboMarca.getSelectedItem();
        String modelo = jTextField3.getText();
        int anio = Integer.parseInt(jTextField4.getText());
        double carga = Double.parseDouble(jTextField5.getText());
        String suc = (String) jComboSucursal.getSelectedItem();
        VehiculoTipo tipo = (VehiculoTipo) jComboTipo.getSelectedItem();
        double kmLitro = Double.parseDouble(jTextField6.getText());
        double litrosExtra = Double.parseDouble(jTextField2.getText());
        
        Controlador.AgregarVehiculo(patente, marcaNombre, modelo, anio, carga, suc, tipo, kmLitro, litrosExtra);
        
        listarVehiculos();
        
        jTextField1.setText("");
        jTextField2.setText("0");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("0");
        inicializarCombos();
        
    }//GEN-LAST:event_jButton1ActionPerformed
private void listarVehiculos(){
        vehiculosGrid.getModel();
        ArrayList<VehiculoViewModel> vehiculos = Controlador.getVehiculos();
        vehiculosGrid.setModel(new DefaultTableModel(new Object[][] {}, 
            new String[] { "Patente","Vehículo", "Tipo", "Sucursal", "Cap.Carga", "Km/litro", "Año", "Litros extra", "Km a recorrer" }));
        
        for(VehiculoViewModel vehiculo : vehiculos){
            ((DefaultTableModel)vehiculosGrid.getModel()).addRow(new Object[] {
                vehiculo.getPatente(),
                vehiculo.getVehiculo(),
                vehiculo.getTipo(),
                vehiculo.getSucursal(),
                vehiculo.getCapacidadCarga(),
                vehiculo.getKmPorLitro(),
                vehiculo.getAnio(),
                vehiculo.getLitrosExtra(),
                vehiculo.getKmARecorrer()
            });
        }
    }
