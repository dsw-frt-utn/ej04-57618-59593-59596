    private void inicializarCombos(){
        jComboSucursal.removeAllItems();
        jComboTipo.removeAllItems();
        jComboMarca.removeAllItems();
        jComboSucursal.addItem("Seleccionar sucursal...");
        jComboMarca.addItem("Seleccionar marca...");
        jComboTipo.addItem("Seleccionar tipo...");
        
        for(Sucursal s : sucursales){
            jComboSucursal.addItem(s.getCodigo());
        }
        for (VehiculoTipo v : VehiculoTipo.values()) {
            jComboTipo.addItem(v);
        }
        for(Marca m : marcas){
            jComboMarca.addItem(m.getNombre());
        }
    }

