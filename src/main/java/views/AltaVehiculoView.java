    private void jComboTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboTipoActionPerformed
        if(jComboTipo.getSelectedItem() == VehiculoTipo.COMBUSTIBLE){
            jLabel7.setVisible(true);
            jLabel10.setVisible(true);
            jTextField6.setVisible(true);
            jTextField2.setVisible(true);
            jTextField6.setText("0");
            jTextField2.setText("0");
        }
        if(jComboTipo.getSelectedItem() == VehiculoTipo.ELECTRICO){
            jLabel7.setVisible(false);
            jLabel10.setVisible(false);
            jTextField6.setVisible(false);
            jTextField2.setVisible(false);
        }
        if(jComboTipo.getSelectedItem() == "Seleccionar tipo..."){
            jLabel7.setVisible(false);
            jLabel10.setVisible(false);
            jTextField6.setVisible(false);
            jTextField2.setVisible(false);
        }
    }//GEN-LAST:event_jComboTipoActionPerformed

